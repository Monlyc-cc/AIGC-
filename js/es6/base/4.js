let str='sawwadawd'
// string.includes() 方法用于判断一个字符串是否包含在另一个字符串中
console.log(str.includes('ad'))

//str.startsWith() 方法用于判断一个字符串是否以指定的子字符串开头
console.log(str.startsWith('sa'))

//str.indexof() 方法用于判断一个字符串是否包含在另一个字符串中
console.log(str.indexOf('ad'))


// string.replace() 方法用于替换字符串中的指定子字符串
console.log(str.replace('w','dd'))
